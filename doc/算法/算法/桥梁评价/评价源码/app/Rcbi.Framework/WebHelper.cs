        public virtual bool IsPostBeingDone
        {
            get
            {
                if (_httpContext.Items["Rcbi.IsPOSTBeingDone"] == null)
                    return false;
                return Convert.ToBoolean(_httpContext.Items["Rcbi.IsPOSTBeingDone"]);
            }
            set
            {
                _httpContext.Items["Rcbi.IsPOSTBeingDone"] = value;
            }
        }

